#include <unistd.h>
#include <stdio.h>

int	ft_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb <= 0)
		return (0);
	while (a < nb / a)
		a++;
	if (a * a == nb)
		return (a);
	else
		return (0);
}
